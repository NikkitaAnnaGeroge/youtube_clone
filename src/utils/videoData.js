export const addCommentToVideo = (videoId, commentText, author = "You") => {
  const newComment = {
    id: `c_${Date.now()}`,
    author: author,
    avatar: `https://api.dicebear.com/7.x/initials/svg?seed=${author}&backgroundColor=ef4444`,
    text: commentText,
    likes: 0,
    publishedAt: "Just now"
  };
  
  if (!localComments[videoId]) {
    getCommentsForVideo(videoId);
  }
  
  localComments[videoId] = [newComment, ...localComments[videoId]];
  return localComments[videoId];
};

// Search & filter helpers
export const queryVideos = (searchTerm) => {
  if (!searchTerm) return videos;
  const term = searchTerm.toLowerCase();
  return videos.filter(video => 
    video.title.toLowerCase().includes(term) ||
    video.description.toLowerCase().includes(term) ||
    video.channelTitle.toLowerCase().includes(term) ||
    video.category.toLowerCase().includes(term)
  );
};

export const getVideosByCategory = (category) => {
  if (!category || category === "All") return videos;
  return videos.filter(video => video.category.toLowerCase() === category.toLowerCase());
};

export const getVideosByChannel = (channelId) => {
  return videos.filter(video => video.channelId === channelId);
};

export const getVideoById = (id) => {
  return videos.find(video => video.id === id);
};
